// Aliases that should be grouped under a canonical country name
const CANONICAL: Record<string, string> = {
  UAE: "United Arab Emirates",
  UK:  "United Kingdom",
  US:  "United States",
  USA: "United States",
};

export function findCountryInText(text: string): (CountryCoords & { name: string }) | null {
  for (const [country, coords] of Object.entries(COUNTRY_COORDS)) {
    if (text.includes(country)) {
      return { ...coords, name: CANONICAL[country] ?? country };
    }
  }
  return null;
}
